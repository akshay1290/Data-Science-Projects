-- Total Cases vs Total Deaths
-- Shows likelihood of dying if you contract covid in your country

SELECT
    Location,
    date,
    total_cases,
    population,
    CASE
        WHEN ISNUMERIC(total_cases) = 1 AND ISNUMERIC(total_deaths) = 1 AND total_cases != 0
        THEN (CAST(total_cases AS FLOAT) / CAST(population AS FLOAT)) * 100
        ELSE NULL
    END AS percentpoplationinfected
FROM portfolioproject..CovidDeaths
where location like '%india'
ORDER BY 1, 2;

-- Total Cases vs Population
-- Shows what percentage of population infected with Covid

SELECT
    Location,
    date,
    total_cases,
    total_deaths,
    CASE
        WHEN ISNUMERIC(total_cases) = 1 AND ISNUMERIC(total_deaths) = 1 AND total_cases != 0
        THEN (CAST(total_deaths AS FLOAT) / CAST(total_cases AS FLOAT)) * 100
        ELSE NULL
    END AS DeathPercentage
FROM portfolioproject..CovidDeaths
where location like '%india'
and continent is not null
ORDER BY 1, 2;


-- Countries with Highest Infection Rate compared to Population

WITH MaxInfection AS (
    SELECT
        Location,
        MAX(total_cases) AS highestinfectioncount
    FROM portfolioproject..CovidDeaths
    GROUP BY Location
)

SELECT
    cd.Location,
    cd.population,
    mi.highestinfectioncount,
    CASE
        WHEN ISNUMERIC(cd.total_cases) = 1 AND ISNUMERIC(cd.total_deaths) = 1 AND cd.total_cases != 0
        THEN (CAST(mi.highestinfectioncount AS FLOAT) / CAST(cd.population AS FLOAT)) * 100
        ELSE NULL
    END AS percentpopulationinfected
FROM portfolioproject..CovidDeaths cd
INNER JOIN MaxInfection mi ON cd.Location = mi.Location
where continent is not null
ORDER BY percentpopulationinfected desc;


